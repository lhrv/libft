#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	char	*str;

	str = s1 + ft_strlen(s1);
	while (n && *s2)
	{
		*str++ = *s2++;
		n--;
	}
	*(str) = '\0';
	return (s1);
}
